d = {'one': 1, 'two': 2, 'three': 3, 4: 4}
d2 = {1: 'кол', 2: 'двояк', 3: 'тройбан', 4: 'хорошо'}

d3 = d | d2
print(d3)   # {'one': 1, 'two': 2, 'three': 3, 4: 'хорошо', 1: 'кол', 2: 'двояк', 3: 'тройбан'}

# Внимание на ключ 4 - значение по ключу из последнего словаря перезапишет предыдущее!
d4 = {**d2, **d}
print(d4)   # {1: 'кол', 2: 'двояк', 3: 'тройбан', 4: 4, 'one': 1, 'two': 2, 'three': 3}
